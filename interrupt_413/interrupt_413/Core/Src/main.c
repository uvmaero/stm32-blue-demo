// Callbacks for the different interrupts and messages

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
  if (htim == &htim13){ // check which timer was called
    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_1);
    
  }
  if (htim == &htim14){ // check with timer was called
    TxData[0] = 100;
    TxData[1] = 2;

    // add and transmit the message
    HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox);
  }
}

// There's "Half" and "Full" buffers we can call on. For us, we want to 
// scan all the pins which will fill up the buffer, so wait until the 
// buffer is full. 
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc){
  
  if (adc_buf[0] >= 2048){
    
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
  }
  else{
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
  }
  if (adc_buf[1] >= 2048){
    
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_SET);
  }
  else{
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_2, GPIO_PIN_RESET);
  }
}

